<?php
    // Informasi Data Diri
    $umur = 25;
    $tinggi = 175.5;
    $menikah = true;
    $nama = "John Doe";

    // Menggunakan %d untuk integer
    printf("Umur: %d tahun\n", $umur);

    // Menggunakan %f untuk float
    printf("Tinggi: %.2f cm\n", $tinggi);

    // Menggunakan %b untuk boolean
    printf("Status Menikah: %b\n", $menikah);

    // Menggunakan %s untuk string
    printf("Nama: %s\n", $nama);
?>
